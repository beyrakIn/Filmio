package com.squad.filmio.api.models.tv;

import com.squad.filmio.api.models.movie.Movie;
import com.squad.filmio.api.models.tv.Tv;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class TvResponse implements Serializable {
    private int page;
    List<Tv> results = new ArrayList<>();
    private int total_pages;
    private int total_results;

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public List<Tv> getResults() {
        return results;
    }

    public void setResults(List<Tv> results) {
        this.results = results;
    }

    public int getTotal_pages() {
        return total_pages;
    }

    public void setTotal_pages(int total_pages) {
        this.total_pages = total_pages;
    }

    public int getTotal_results() {
        return total_results;
    }

    public void setTotal_results(int total_results) {
        this.total_results = total_results;
    }

    @Override
    public String toString() {
        return "SearchTv{" +
                "page=" + page +
                ", results=" + results +
                ", total_pages=" + total_pages +
                ", total_results=" + total_results +
                '}';
    }
}
